		static Dictionary<string, string> dashedPairToChannelName = new Dictionary<string, string>();
		static Dictionary<string, AvailableTrades> availableTrades = new Dictionary<string, AvailableTrades>();

		static BinanceSymbolLookup()
		{
			AddPairs();
		}

		public static AvailableTrades GetAvailableTrades(string symbolName)
		{
			if (availableTrades.ContainsKey(symbolName))
				return availableTrades[symbolName];
			return null;
		}

		static void AddPair(string binanceSymbol, string dashedPair)
		{
			int indexOfDash = dashedPair.IndexOf("-");
			if (indexOfDash > 0)
			{
				string left = dashedPair.Substring(0, indexOfDash);
				string right = dashedPair.Substring(indexOfDash + 1);
				if (!availableTrades.ContainsKey(left))
					availableTrades[left] = new AvailableTrades(left);
				if (!availableTrades.ContainsKey(right))
					availableTrades[right] = new AvailableTrades(right);
				availableTrades[left].AddBinanceTradingChannel(right, binanceSymbol);
				availableTrades[right].AddBinanceTradingChannel(left, binanceSymbol);
			}

			channelNameToDashedPair[binanceSymbol] = dashedPair;
			dashedPairToChannelName[dashedPair] = binanceSymbol;
		}

